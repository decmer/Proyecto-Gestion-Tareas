    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextFieldNombre.getText();
        String descripcion = jTextFieldDescripcion.getText();
        int prioridad = jComboBoxPriority.getSelectedIndex()+1;
        Managers m = (Managers)manager;
        Date fL = (Date)jSpinnerDate.getValue();
        int idPro = Integer.parseInt(main.mMP.getValueAt(main.rowSelect, 0)+"");
        Proyectos p;
        
        Query q = BaseDatos.getBBDD().query();
        q.constrain(Proyectos.class);
        q.descend("id").constrain(idPro).equal();
        ObjectSet oS = q.execute();
        if (oS.hasNext()) {
            p = (Proyectos)oS.next();
        }
        else{
            JOptionPane.showMessageDialog(null,"fallo en la seleccion del proyecto","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            manager.crearTarea(nombre, prioridad, p, descripcion, Estados.EN_ESPERA, new Date(fL.getYear(), fL.getMonth(), fL.getDay()));
        } catch (ExcepcionPer ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        main.iniciarTablaManagerTareas(idPro);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

