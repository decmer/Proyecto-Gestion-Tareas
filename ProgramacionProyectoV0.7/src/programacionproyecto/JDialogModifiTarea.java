    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextFieldNombre.getText();
        String descripcion = jTextFieldDescripcion.getText();
        int prioridad = jComboBoxPriority.getSelectedIndex()+1;
        Managers m = (Managers)manager;
        Date fL = (Date)jSpinnerDate.getValue();
        int idPro = Integer.parseInt(jTextFieldIdProyecto.getText());
        
        Estados estado = Estados.valueOf(jComboBoxEstado.getSelectedItem()+"");
        Proyectos p;
        Desarrolladores d = null;
        
        if (!jTextFieldIdDessarrollador.getText().equals("NULL")) {
            int idDesa = Integer.parseInt(jTextFieldIdDessarrollador.getText());
            Query qD = BaseDatos.getBBDD().query();
            qD.constrain(Desarrolladores.class);
            qD.descend("id").constrain(idDesa).equal();
            ObjectSet oSD = qD.execute();
            if (oSD.hasNext()) {
                d = (Desarrolladores)oSD.next();
            }
            else{
                JOptionPane.showMessageDialog(null,"fallo en la seleccion del Dessarroollador","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        
        Query q = BaseDatos.getBBDD().query();
        q.constrain(Proyectos.class);
        q.descend("id").constrain(idPro).equal();
        ObjectSet oS = q.execute();
        if (oS.hasNext()) {
            p = (Proyectos)oS.next();
        }
        else{
            JOptionPane.showMessageDialog(null,"fallo en la seleccion del proyecto","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            manager.modificarTarea(tModi, nombre, prioridad, p, descripcion, estado, d, new Date(fL.getYear(), fL.getMonth(), fL.getDay()));
        } catch (ExcepcionPer ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
        main.iniciarTablaManagerTareas(idPro);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

