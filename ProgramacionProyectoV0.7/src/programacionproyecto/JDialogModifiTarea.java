    public JDialogModifiTarea(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        main = ((JFrame)parent);
        manager = (Managers)((JFrame)parent).usu;
        initComponents();
        Query qD = BaseDatos.getBBDD().query();
        qD.constrain(Tareas.class);
        qD.descend("id").constrain(Integer.parseInt(main.mMT.getValueAt(main.rowSelectAux, 0)+"")).equal();
        ObjectSet oSD = qD.execute();
        while (oSD.hasNext()) {
            Tareas t = (Tareas)oSD.next();
            if (t.getPerteneProyecto().getId() == Integer.parseInt(main.mMP.getValueAt(main.rowSelect, 0)+"")) {
                tModi = t;
                jTextFieldNombre.setText(t.getNombre());
                jTextFieldDescripcion.setText(t.getDescripcion());
                if (t.getDesarrollador() != null) {
                    jTextFieldIdDessarrollador.setText(t.getDesarrollador().getId()+"");
                }
                jTextFieldIdProyecto.setText(t.getPerteneProyecto().getId()+"");
                jComboBoxPriority.setSelectedIndex(t.getPrioridad()-1);
                switch(t.getEstado().getValor()){
                    case "EN_ESPERA":
                        jComboBoxEstado.setSelectedIndex(0);
                        break;
                    case "EN_REALIZACION":
                        jComboBoxEstado.setSelectedIndex(1);
                        break;
                    case "TERMINADO":
                        jComboBoxEstado.setSelectedIndex(2);
                        break;
                }
                break;
            }
        }
    }

