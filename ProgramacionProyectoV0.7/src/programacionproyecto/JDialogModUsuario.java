    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (admin.modificarUsuario(usuModifi , jTextFieldNombre.getText(), jTextFieldContrasena.getText())){
            
            int idusu = Integer.parseInt(m.getValueAt(rowSelect, 0)+"");
            Query q = BaseDatos.getBBDD().query();
            q.constrain(Usuarios.class);
            q.descend("id").constrain(idusu);
            ObjectSet o = q.execute();
            if(o.hasNext()){
                m.setValueAt(jTextFieldNombre.getText(), rowSelect, 1);
                m.setValueAt(jTextFieldContrasena.getText(), rowSelect, 2);
                admin.modificarUsuario((Usuarios)o.next() ,jTextFieldNombre.getText(), jTextFieldContrasena.getText());
                dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

